let i =0;
// if: ejecuta algo UNA VEZ si se cumple una condición
if(i<5){
    console.log('estoy en el if: '+i+' es menor que 5');
    i++;
}
console.log('después del if i vale '+i);

// while: ejecuta algo en bucle mientras se cumpla una condición
while(i<5) {
    console.log('estoy en el while: '+i+ ' es menor que 5');
    i++;
}
console.log('después del while i vale '+i);